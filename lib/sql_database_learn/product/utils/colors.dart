import 'package:flutter/material.dart';

class ProjectColors extends Color {
  ProjectColors.whiteColor() : super.fromRGBO(255, 255, 255, 1);
  ProjectColors.grenShade() : super.fromRGBO(76, 175, 80, 1);
  ProjectColors.amberShade() : super.fromRGBO(255, 111, 0, 1);
  ProjectColors.blueShade() : super.fromRGBO(21, 101, 192, 1);
  ProjectColors.blueShade200() : super.fromRGBO(144, 202, 249, 1);
  ProjectColors.greyShade() : super.fromARGB(255, 158, 158, 158);
}
